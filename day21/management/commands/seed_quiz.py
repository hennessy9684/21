class Command(BaseCommand):
    help = '导入安全知识测评题库种子数据（跳过已存在的题目）'

    def handle(self, *args, **options):
        existing_texts = set(QuizQuestion.objects.values_list('question', flat=True))
        added = 0
        skipped = 0

        for row in SEED_DATA:
            day, q_type, question, a, b, c, d, ans, exp = row
            if question in existing_texts:
                skipped += 1
                continue
            QuizQuestion.objects.create(
                q_type=q_type, day=day, question=question,
                option_a=a, option_b=b, option_c=c, option_d=d,
                answer=ans, explanation=exp,
            )
            added += 1

        self.stdout.write(self.style.SUCCESS(
            f'完成！新增 {added} 道题，跳过(已存在) {skipped} 道题'
        ))
        self.stdout.write(f'题库总题数: {QuizQuestion.objects.count()}')
