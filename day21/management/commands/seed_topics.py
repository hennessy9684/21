class Command(BaseCommand):
    help = '初始化21天打卡主题（幂等操作，已存在的主题不会重复创建）'

    def handle(self, *args, **options):
        existing_days = set(DailyTopic.objects.values_list('day', flat=True))
        created = 0
        skipped = 0

        for day, title, content, question, icon in TOPICS_DATA:
            if day in existing_days:
                skipped += 1
                continue
            DailyTopic.objects.create(
                day=day, title=title, content=content,
                question=question, icon=icon,
            )
            created += 1

        self.stdout.write(self.style.SUCCESS(
            f'完成！新增 {created} 个主题，跳过(已存在) {skipped} 个'
        ))
        self.stdout.write(f'主题总数: {DailyTopic.objects.count()}')
